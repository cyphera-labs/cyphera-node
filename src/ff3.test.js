const { describe, it } = require("node:test");
const assert = require("node:assert/strict");
const { FF3, DIGITS } = require("./ff3");

const hex = (s) => Buffer.from(s, "hex");
const R26 = "0123456789abcdefghijklmnop";

function nist(keyHex, tweakHex, alphabet, pt, ct) {
  const c = new FF3(hex(keyHex), hex(tweakHex), alphabet);
  assert.equal(c.encrypt(pt), ct, `encrypt(${pt})`);
  assert.equal(c.decrypt(ct), pt, `decrypt(${ct})`);
}

describe("FF3 NIST Vectors", () => {
  it("s01", () => nist("EF4359D8D580AA4F7F036D6F04FC6A94", "D8E7920AFA330A73", DIGITS, "890121234567890000", "750918814058654607"));
  it("s02", () => nist("EF4359D8D580AA4F7F036D6F04FC6A94", "9A768A92F60E12D8", DIGITS, "890121234567890000", "018989839189395384"));
  it("s03", () => nist("EF4359D8D580AA4F7F036D6F04FC6A94", "D8E7920AFA330A73", DIGITS, "89012123456789000000789000000", "48598367162252569629397416226"));
  it("s04", () => nist("EF4359D8D580AA4F7F036D6F04FC6A94", "0000000000000000", DIGITS, "89012123456789000000789000000", "34695224821734535122613701434"));
  it("s05", () => nist("EF4359D8D580AA4F7F036D6F04FC6A94", "9A768A92F60E12D8", R26, "0123456789abcdefghi", "g2pk40i992fn20cjakb"));
  it("s06", () => nist("EF4359D8D580AA4F7F036D6F04FC6A942B7E151628AED2A6", "D8E7920AFA330A73", DIGITS, "890121234567890000", "646965393875028755"));
  it("s07", () => nist("EF4359D8D580AA4F7F036D6F04FC6A942B7E151628AED2A6", "9A768A92F60E12D8", DIGITS, "890121234567890000", "961610514491424446"));
  it("s08", () => nist("EF4359D8D580AA4F7F036D6F04FC6A942B7E151628AED2A6", "D8E7920AFA330A73", DIGITS, "89012123456789000000789000000", "53048884065350204541786380807"));
  it("s09", () => nist("EF4359D8D580AA4F7F036D6F04FC6A942B7E151628AED2A6", "0000000000000000", DIGITS, "89012123456789000000789000000", "98083802678820389295041483512"));
  it("s10", () => nist("EF4359D8D580AA4F7F036D6F04FC6A942B7E151628AED2A6", "9A768A92F60E12D8", R26, "0123456789abcdefghi", "i0ihe2jfj7a9opf9p88"));
  it("s11", () => nist("EF4359D8D580AA4F7F036D6F04FC6A942B7E151628AED2A6ABF7158809CF4F3C", "D8E7920AFA330A73", DIGITS, "890121234567890000", "922011205562777495"));
  it("s12", () => nist("EF4359D8D580AA4F7F036D6F04FC6A942B7E151628AED2A6ABF7158809CF4F3C", "9A768A92F60E12D8", DIGITS, "890121234567890000", "504149865578056140"));
  it("s13", () => nist("EF4359D8D580AA4F7F036D6F04FC6A942B7E151628AED2A6ABF7158809CF4F3C", "D8E7920AFA330A73", DIGITS, "89012123456789000000789000000", "04344343235792599165734622699"));
  it("s14", () => nist("EF4359D8D580AA4F7F036D6F04FC6A942B7E151628AED2A6ABF7158809CF4F3C", "0000000000000000", DIGITS, "89012123456789000000789000000", "30859239999374053872365555822"));
  it("s15", () => nist("EF4359D8D580AA4F7F036D6F04FC6A942B7E151628AED2A6ABF7158809CF4F3C", "9A768A92F60E12D8", R26, "0123456789abcdefghi", "p0b2godfja9bhb7bk38"));
});
